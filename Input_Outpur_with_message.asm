.MODEL SMALL
.STACK 100H
.DATA
A DB 'ENTER YOUR NUMBER: $'
B DB 'YOUR NUMBER IS: $'
.CODE

MAIN PROC
    MOV AX,@DATA
    MOV DS,AX
    
    MOV AH,09H
    LEA DX,A
    INT 21H
    
    MOV AH,01H
    INT 21H
    MOV BL,AL
    
    MOV AH,02H
    MOV DL,0AH
    INT 21H
    MOV DL,0DH
    INT 21H
    
    MOV AH,09
    LEA DX,B
    INT 21H
    
    MOV AH,02
    MOV DL,BL
    INT 21H  
    MAIN ENDP
END MAIN
