.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB 0AH,0DH,'ENTER YOUR CHARACTER:$'
MSG2 DB 0AH,0DH,'NUMBER BIT REPRESENTATION:$'
MSG3 DB 0AH,0DH,'NUMBER OF 1:$'
.CODE

MAIN PROC
    MOV AX,@DATA
    MOV DS,AX
    MOV AH,09H
    LEA DX,MSG1
    INT 21H
    MOV AH,01H
    INT 21H
    MOV BL,AL
    MOV CX,8
    MOV BH,0
    MOV AH,09H
    LEA DX,MSG2
    INT 21H
L1:
    SHL BL,1
    JC L2
    MOV AH,02H
    MOV DL,'0'
    INT 21H 
    JMP L3
L2: 
    MOV AH,02H
    MOV DL,'1'
    INC BH
    INT 21H
L3:
    LOOP L1
    
    ADD BH,'0'
    MOV AH,09H
    LEA DX,MSG3
    INT 21H
    MOV DL,BH
    MOV AH,02H
    INT 21H
    MAIN ENDP
END MAIN